public class MaxInfo
{

    private int max,index;

    public MaxInfo()
    { this.max = 0; this.index = 0;}

    public void setMax(int _max)
    {
        this.max = _max;
    }
    public void setIndex(int _index)
    {
        this.index = _index;
    }

    public int getMax()
    {
        return this.max;
    }
    public int getIndex()
    {
        return this.index;
    }

}
